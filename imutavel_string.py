palavra = "alura"
palavra.upper()
print(palavra) #qual é o resultado?

# A resposta é "alura" com letras minúsculas! Isto é porque o upper sempre
# devolve uma nova string e não altera a string original. Essa regra tambem aplica
# para todas as outras funcoes do tipo str!
# Esse principio tambem e chamado de imutabilidade
# Strings sao imutaveis, sao sequencias imutaveis!
